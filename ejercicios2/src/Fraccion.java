public class Fraccion {

    public static double sumar(double f1, double f2) {
        return f1 + f2;
    }

    public static double sumar(double f, int n) {
        return f + n;
    }

    public static double restar(double f1, double f2) {
        return f1 - f2;
    }

    public static double restar(double f, int n) {
        return f - n;
    }

    public static double multiplicar(double f1, double f2) {
        return f1 * f2;
    }

    public static double multiplicar(double f, int n) {
        return f * n;
    }

    public static double dividir(double f1, double f2) {
        return f1 / f2;
    }

    public static double dividir(double f, int n) {
        return f / n;
    }
}
